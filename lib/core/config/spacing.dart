/// Standard spacing constants used throughout the app.
///
/// Based on a 4px grid system for consistent visual rhythm.
/// Use these instead of hardcoding numeric padding/margin values.
abstract final class AppSpacing {
  /// 4px - Minimal spacing between tightly related elements.
  static const double xs = 4;

  /// 6px - Small spacing for compact layouts.
  static const double sm = 6;

  /// 8px - Spacing for related elements within a group.
  static const double md = 8;

  /// 12px - Spacing between sub-sections or list items.
  static const double lg = 12;

  /// 16px - Standard section spacing and screen padding.
  static const double xl = 16;

  /// 20px - Extended padding for prominent sections.
  static const double xxl = 20;

  /// 24px - Spacing between major sections.
  static const double xxxl = 24;

  /// 32px - Large spacing for hero/empty state layouts.
  static const double huge = 32;

  /// Standard horizontal screen padding.
  static const double screenHorizontal = 16;

  /// Standard vertical screen padding.
  static const double screenVertical = 8;

  /// Card internal padding.
  static const double cardPadding = 16;

  /// Card margin.
  static const double cardMargin = 8;
}
